public class Course {
  private String name;
  private String area;

  public Course() {
  }

  public Course(String name, String area) {
    this.name = name;
    this.area = area;
  }

  public String getName() {
    return this.name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getArea() {
    return this.area;
  }

  public void setArea(String area) {
    this.area = area;
  }
  

  
}
